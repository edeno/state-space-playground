# %% [markdown]
# ## Notes
#
# - **DIM is the most expressive oscillator model** in the hierarchy
#   (COM < CNM < DIM). COM: measurement matrix switches. CNM: process
#   covariance switches. DIM: transition matrix switches (directed
#   coupling). DIM is the right choice for "does functional connectivity
#   change between ripple and non-ripple states?"
#
# - **The `edge_l1` penalty** encourages sparse coupling. Without it,
#   SGD will fill in small nonzero couplings between all pairs. With
#   `edge_l1=0.01`, most couplings should be driven near zero, leaving
#   only the strongest connections visible. Tuning this value is a
#   model-selection question — too high kills all coupling, too low
#   doesn't sparsify.
#
# - **Standardization matters.** The LFP is standardized to zero-mean
#   unit-variance per channel before fitting. Without this, channels
#   with higher variance dominate the likelihood and the oscillator
#   parameters converge to match the highest-power channel only.
#
# - **Time resolution for state switching.** The discrete state
#   transitions happen at the LFP sampling rate (1 kHz). Ripple events
#   last ~70 ms median (70 samples). The model needs enough temporal
#   resolution to capture transitions into and out of ripple states
#   within a single event.
#
# - **Scaling.** 4 channels × 60 seconds × 1 kHz = 60,000 time bins.
#   The switching Kalman filter's scan body runs per bin. At ~200 SGD
#   steps this is ~12M filter evaluations. Runtime depends on n_osc
#   and n_states but should be manageable (~minutes to tens of minutes).
#
# - **What to look for in the plots:**
#   - State probabilities should track ripple events (one state high
#     during orange-shaded periods, the other high otherwise).
#   - Coupling matrices should differ between states (sparse in non-
#     ripple, denser or differently structured during ripple).
#   - State-conditioned PSDs should show elevated 150-250 Hz power
#     in the "ripple state" relative to the "background state."
#
