# %% [markdown]
# ### Per-state power spectra (state-conditioned)
#
# For each time bin, weight the power by the posterior state probability
# and average across the session. If the states separate ripple from
# non-ripple, one state should show elevated 150-250 Hz power.

# %%
from scipy.signal import welch  # noqa: E402

# Compute state-conditioned power spectra
fig, axes = plt.subplots(1, N_CHANNELS, figsize=(3.5 * N_CHANNELS, 3.5),
                         sharex=True, sharey=True)
if N_CHANNELS == 1:
    axes = [axes]

# Split the LFP into short segments and assign each segment a state
segment_len = int(0.1 * lfp_fs)  # 100 ms segments
n_segments = n_time // segment_len

for ch_idx in range(N_CHANNELS):
    ch_data = lfp_normed[:n_segments * segment_len, ch_idx]
    segments = ch_data.reshape(n_segments, segment_len)

    # Average state probability per segment
    seg_disc = disc_probs[:n_segments * segment_len].reshape(n_segments, segment_len, -1).mean(axis=1)

    for s in range(N_DISCRETE_STATES):
        weights = seg_disc[:, s]
        # Weighted average of per-segment PSDs
        psds = []
        for seg_i in range(n_segments):
            f, psd = welch(segments[seg_i], fs=lfp_fs, nperseg=min(segment_len, 256))
            psds.append(psd * weights[seg_i])
        avg_psd = np.mean(psds, axis=0) / np.mean(weights)
        axes[ch_idx].semilogy(f, avg_psd, lw=1.2, label=f"state {s}", alpha=0.8)

    axes[ch_idx].set_title(f"ch {selected_channels[ch_idx]}", fontsize=9)
    axes[ch_idx].set_xlabel("freq (Hz)")
    axes[ch_idx].axvspan(150, 250, color="orange", alpha=0.1)
    if ch_idx == 0:
        axes[ch_idx].set_ylabel("PSD (a.u.)")
        axes[ch_idx].legend(fontsize=7)
    axes[ch_idx].grid(True, alpha=0.3)

fig.suptitle("State-conditioned power spectra (orange = ripple band)", y=1.02)
plt.tight_layout()
plt.show()

